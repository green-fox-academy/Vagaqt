public class TwoNumbers {
    public static void main(String[] args) {
        // Create a program that prints a few operations on two numbers: 22 and 13

        // Print the result of 13 added to 22

        // Print the result of 13 substracted from 22

        // Print the result of 22 multiplied by 13

        // Print the result of 22 divided by 13 (as a decimal fraction)

        // Print the integer part of 22 divided by 13

        // Print the remainder of 22 divided by 13

        System.out.println("13 added to 22 is :" + (13 + 22));
        System.out.println("13 subtracted from 22 is :" + (22 - 13));
        System.out.println("22 multiplied by 13 is :" + (22 * 13));
        System.out.println("22 divided by 13 (as a decimal fraction) is :" + (22.0 / 13));
        System.out.println("The integer part of 22 divided by 13 is :" + (22 / 13));
        System.out.println("The remainder of 22 divided by 13 is :" + (22 % 13));
    }
}
