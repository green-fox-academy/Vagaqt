public class Fox {
    String name;
    String colour;
    Integer age;

    public Fox(String name,String colour, Integer age) {
        this.name = name;
        this.colour = colour;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getColour(){
        return colour;
    }

    public Integer getAge() {
        return age;
    }
}

